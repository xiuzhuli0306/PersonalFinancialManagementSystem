/**
 * 得到完整的时间格式 年-月-日 时分秒
 */
export function formatYMDHMS(val) {
  return window.formatYMDHMS(val);
}
/**
 * 得到完整的时间格式 年-月-日
 */
export function formatYMD(val) {
  return window.formatYMD(val);
}

/**
 * 得到时分秒格式
 */
export function formatHMS(val) {
  return window.formatHMS(val);
}
